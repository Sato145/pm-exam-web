@app.route('/submit_answer', methods=['POST'])
def submit_answer():
    data = request.json
    selected_answer = int(data['answer'])
    
    questions = session.get('current_questions', [])
    current_index = session.get('current_question_index', 0)
    correct_answers = session.get('correct_answers', 0)
    
    if current_index < len(questions):
        current_question = questions[current_index]
        is_correct = selected_answer == current_question['answer']
        
        if is_correct:
            correct_answers += 1
            session['correct_answers'] = correct_answers
        
        response = {
            'correct': is_correct,
            'correct_answer': current_question['answer'],
            'explanation': current_question['explanation']
        }
        
        # 次の問題に進む
        session['current_question_index'] = current_index + 1
        
        if current_index + 1 < len(questions):
            response['next_question'] = questions[current_index + 1]
            response['question_num'] = current_index + 2
            response['has_next'] = True
        else:
            # 最後の問題
            score = (correct_answers / len(questions)) * 100
            response['has_next'] = False
            response['final_score'] = {
                'correct': correct_answers,
                'total': len(questions),
                'percentage': score
            }
            # スコアを保存
            save_score('午前', score)
        
        return jsonify(response)
    
    return jsonify({'error': 'Invalid request'})

